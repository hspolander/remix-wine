const db = new PrismaClient();

async function seedWines() {
  await db.user.deleteMany();
  await db.wineUser.deleteMany();
  await db.wine.deleteMany();
  await db.systembolagetWine.deleteMany();
  await db.systembolagetTasteSymbol.deleteMany();
  await db.wineGrape.deleteMany();
  await db.systembolagetGrape.deleteMany();

  const wineUser = await db.wineUser.create({
    data: {
      username: "Chenin",
      name: "Mauritz",
      // this is a hashed version of "cheninBlanc"
      passwordHash:
        "$2a$10$bL82pL4z1vwlAIDYsx.1FOzPDjGu.oXJpMTL8KXDEWPkcJuDiKVfm",
    },
  });

  getSysWines().map(async (sysWine) => {
    const {
      productId,
      alcoholPercentage,
      assortmentText,
      bottleText,
      categoryLevel1,
      categoryLevel2,
      categoryLevel3,
      categoryLevel4,
      country,
      customCategoryTitle,
      image,
      isOrganic,
      isSustainableChoice,
      isClimateSmartPackaging,
      grapes,
      originLevel1,
      originLevel2,
      packagingLevel1,
      price,
      producerName,
      productLaunchDate,
      productNameBold,
      productNameThin,
      productNumber,
      productNumberShort,
      sugarContent,
      sugarContentGramPer100ml,
      supplierName,
      taste,
      tasteClockBitter,
      tasteClockBody,
      tasteClockCasque,
      tasteClockFruitacid,
      tasteClockRoughness,
      tasteClockSmokiness,
      tasteClockSweetness,
      tasteSymbols,
      usage,
      vintage,
      volume,
      volumeText,
    } = sysWine;

    await db.systembolagetWine.create({
      data: {
        productId,
        alcoholPercentage,
        assortmentText,
        bottleText,
        categoryLevel1,
        categoryLevel2,
        categoryLevel3,
        categoryLevel4,
        country,
        customCategoryTitle,
        image,
        isOrganic,
        isSustainableChoice,
        isClimateSmartPackaging,
        originLevel1,
        originLevel2,
        packagingLevel1,
        price,
        producerName,
        productLaunchDate,
        productNameBold,
        productNameThin,
        productNumber,
        productNumberShort,
        sugarContent,
        sugarContentGramPer100ml,
        supplierName,
        taste,
        tasteClockBitter,
        tasteClockBody,
        tasteClockCasque,
        tasteClockFruitacid,
        tasteClockRoughness,
        tasteClockSmokiness,
        tasteClockSweetness,
        usage,
        vintage,
        volume,
        volumeText,
      },
    });

    tasteSymbols.forEach(async (tasteSymbol) => {
      await db.systembolagetTasteSymbol.create({
        data: { symbol: tasteSymbol, sysWineId: productId },
      });
    });

    const wineLinkText = productNameBold.replaceAll(" ", "-");
    const url = `https://www.systembolaget.se/produkt/vin/${wineLinkText}-${productNumber}/`;
    const name = `${productNameBold}, ${productNameThin}`;

    const wine = await db.wine.create({
      data: {
        country,
        color: categoryLevel1,
        boughtFrom: "Systembolaget",
        name,
        priceBottle: price,
        producer: producerName,
        vintage,
        sysWineId: productId,
        url,
      },
    });

    grapes.forEach(async (grape) => {
      await db.wineGrape.create({
        data: { name: grape, wineId: wine.id },
      });
    });

    const randomReviews = [
      { ...getRandomReview(), userId: wineUser.id, wineId: wine.id },
      { ...getRandomReview(), userId: wineUser.id, wineId: wine.id },
    ];

    randomReviews.forEach(async (review) => {
      await db.review.create({
        data: review,
      });
    });
  });
}

